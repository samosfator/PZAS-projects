//Дано ціле число N (> 0). Знайти значення квадрату за формулою N^2 = 1 + 3 + 5 + . + (2*N - 1). Після додавання до суми кожного доданку виводити поточне значення суми (в результаті будуть виведені квадрати всіх цілих чисел від 1 до N).

var n = 12,
	sum = 0;
for (var i = 1; i < n; i += 2) {
	sum += i;
	console.log("N^2 =", sum);
}
sum += 2 * n - 1;
console.log("Результат: N^2 =", sum);