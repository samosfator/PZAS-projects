//Дано дійсне число А і ціле число N (> 0). Використовуючи один цикл, знайти значення виразу
//1 - А + A^2 - A^3 + . + (-1)^N * A^N. Умовний оператор не використовувати.

var a = 8.9,
	n = 12,
	result = 1;
for (var i = 1; i <= n; i++) {
	result += Math.pow(-1, i) * Math.pow(a, i);
}
console.log("Результат:", result);